namespace Heirloom.Drawing
{
    /// <summary>
    /// Represents the visual appearance of the corner draw between two lines.
    /// </summary>
    public enum LineJoinType
    {
        /// <summary>
        /// No corner is drawn.
        /// </summary>
        None,

        /// <summary>
        /// Lines are joined with a simple bevel, directly connecting opposing vertices.
        /// </summary>
        Bevel
    }
}
