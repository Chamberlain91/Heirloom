namespace Meadows.Desktop
{
    /// <summary>
    /// Enumeration of standard cursors.
    /// </summary>
    public enum StandardCursor
    {
        /// <summary>
        /// The standard arrow cursor.
        /// </summary>
        Arrow = 0x00036001,

        /// <summary>
        /// The standard IBeam (text edit) cursor.
        /// </summary>
        IBeam = 0x00036002,

        /// <summary>
        /// The standard crosshair cursor.
        /// </summary>
        Crosshair = 0x00036003,

        /// <summary>
        /// The standard hand cursor.
        /// </summary>
        Hand = 0x00036004,

        /// <summary>
        /// The standard horizontal resize cursor.
        /// </summary>
        HResize = 0x00036005,

        /// <summary>
        /// The standard vertical resize cursor.
        /// </summary>
        VResize = 0x00036006,
    }
}
