namespace Heirloom.Platforms.Desktop
{
    public enum WindowState
    {
        /// <summary>
        /// Window is minified (also called iconified).
        /// </summary>
        Minimized,

        /// <summary>
        /// Window is maximized.
        /// </summary>
        Maximized,

        /// <summary>
        /// Window is normal.
        /// </summary>
        Normal,

        /// <summary>
        /// Window is fullscreen.
        /// </summary>
        Fullscreen
    }
}
