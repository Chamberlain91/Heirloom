namespace Meadows
{
    /// <summary>
    /// Represents the various axis on a standard game pad.
    /// </summary>
    /// <category>User Input</category>
    public enum GamepadAxis
    {
        /// <summary>
        /// The left horizontal axis.
        /// </summary>
        LeftHorizontal,

        /// <summary>
        /// The left vertical axis.
        /// </summary>
        LeftVertical,

        /// <summary>
        /// The right horizontal axis.
        /// </summary>
        RightHorizontal,

        /// <summary>
        /// The right vertical axis.
        /// </summary>
        RightVertical,

        /// <summary>
        /// The virtual horizontal axis of the d-pad.
        /// </summary>
        PadHorizontal,

        /// <summary>
        /// The virtual vertical axis of the d-pad.
        /// </summary>
        PadVertical,

        /// <summary>
        /// The left trigger axis.
        /// </summary>
        LeftTrigger,

        /// <summary>
        /// The right trigger axis.
        /// </summary>
        RightTrigger,
    }
}
