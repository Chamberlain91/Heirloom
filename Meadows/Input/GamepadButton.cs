namespace Meadows.Input
{
    /// <summary>
    /// Represents the buttons on a standard gamepad.
    /// </summary>
    /// <category>User Input</category>
    public enum GamepadButton
    {
        /// <summary>
        /// The "A" button.
        /// </summary>
        A,

        /// <summary>
        /// The "B" button.
        /// </summary>
        B,

        /// <summary>
        /// The "X" button.
        /// </summary>
        X,

        /// <summary>
        /// The "Y" button.
        /// </summary>
        Y,

        /// <summary>
        /// The left bumper button.
        /// </summary>
        LeftBumper,

        /// <summary>
        /// The right bumper button.
        /// </summary>
        RightBumper,

        /// <summary>
        /// The back button.
        /// </summary>
        Back,

        /// <summary>
        /// The start button.
        /// </summary>
        Start,

        /// <summary>
        /// The guide or logo button (possibly not supported).
        /// </summary>
        Guide,

        /// <summary>
        /// The 'click' of the left thumbstick.
        /// </summary>
        LeftThumb,

        /// <summary>
        /// The 'click' of the right thumbstick.
        /// </summary>
        RightThumb,

        /// <summary>
        /// The up button on the directional pad.
        /// </summary>
        PadUp,

        /// <summary>
        /// The down button on the directional pad.
        /// </summary>
        PadDown,

        /// <summary>
        /// The left button on the directional pad.
        /// </summary>
        PadLeft,

        /// <summary>
        /// The right button on the directional pad.
        /// </summary>
        PadRight
    }
}
