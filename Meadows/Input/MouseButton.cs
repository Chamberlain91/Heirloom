namespace Meadows.Input
{
    /// <summary>
    /// Represents the buttons on a mouse.
    /// </summary>
    /// <category>User Input</category>
    public enum MouseButton
    {
        /// <summary>
        /// The left mouse button (aka, button 1).
        /// </summary>
        Left,

        /// <summary>
        /// The right mouse button (aka, button 2).
        /// </summary>
        Right,

        /// <summary>
        /// The middle mouse button (aka, button 3).
        /// </summary>
        Middle,

        /// <summary>
        /// Mouse button 4.
        /// </summary>
        Button4,

        /// <summary>
        /// Mouse button 5.
        /// </summary>
        Button5,

        /// <summary>
        /// Mouse button 6.
        /// </summary>
        Button6,

        /// <summary>
        /// Mouse button 7.
        /// </summary>
        Button7,

        /// <summary>
        /// Mouse button 8.
        /// </summary>
        Button8
    }
}
