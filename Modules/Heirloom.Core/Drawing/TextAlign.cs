namespace Heirloom.Drawing
{
    /// <summary>
    /// Controls how text is aligned to the layout rectangle.
    /// </summary>
    public enum TextAlign
    {
        /// <summary>
        /// Text is aligned to the left.
        /// </summary>
        Left,

        /// <summary>
        /// Text is aligned to the center.
        /// </summary>
        Center,

        /// <summary>
        /// Text is aligned to the right.
        /// </summary>
        Right
    }
}
